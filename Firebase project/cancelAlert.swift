import UIKit

extension UIAlertController {
    func cancelAction ()  {
        let cancelAction = UIAlertAction(title: "cancel", style: .cancel, handler: nil)
         self.addAction(cancelAction)
    
    
    }
    
    
    
    
    
    
}
